    public void fillToFormKM(int index) {
        txtMaKhuyenMai.setText(tblKhuyenMai.getValueAt(index, 0).toString());
        txtTenChienDich.setText(tblKhuyenMai.getValueAt(index, 1).toString());
        txtMucGiamGia.setText(tblKhuyenMai.getValueAt(index, 2).toString());
        txtHDTT.setText(tblKhuyenMai.getValueAt(index, 3).toString());
        txtSoLuong.setText(tblKhuyenMai.getValueAt(index, 4).toString());
        Date NgayBatDau = (Date) tblKhuyenMai.getValueAt(index, 6);
        dcsBatDau.setDate(NgayBatDau);
        Date NgayKetThuc = (Date) tblKhuyenMai.getValueAt(index, 7);
        dcsKetThuc.setDate(NgayKetThuc);
        if (tblKhuyenMai.getValueAt(index, 8).toString().equals("Đã hết hạn")) {
            rdoDaHetHan.isSelected();
        } else {
            rdoDangHoatDong.isSelected();
        }
    }

    public KhuyenMai getKhuyenMai() {
        if (txtTenChienDich.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống tên chiến dịch");
            return null;
        }
        if (txtMucGiamGia.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống mức giảm giá");
            return null;
        }
        int MucGiam = 0;
        try {
            MucGiam = Integer.parseInt(txtMucGiamGia.getText());
            if (MucGiam < 0) {
                JOptionPane.showMessageDialog(this, "Mức giảm không được âm");
                return null;
            }
            // Nếu yêu cầu chỉ đúng khi MucGiam >= 0 và <= 50
            if (MucGiam > 50) {
                JOptionPane.showMessageDialog(this, "Mức giảm phải nhỏ hơn hoặc bằng 50");
                return null;
            }
            // ...

            if (txtHDTT.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn không được để trống Hóa đơn tối thiểu");
                return null;
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mức giảm phải là số nguyên");
            return null;
        }
        float HDTT = 0.0f; // Khởi tạo giá trị HDTT ở đây
        try {
            HDTT = Float.parseFloat(txtHDTT.getText());
            if (HDTT < MucGiam) {
                JOptionPane.showMessageDialog(this, "Hóa đơn tối thiểu phải lớn hơn mức giảm");
                return null;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Hóa đơn tối thiểu phải là số thực");
            return null;
        }
        if (txtSoLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn không được để trống số lượng");
            return null;
        }
        int soLuong;
        try {
            soLuong = Integer.parseInt(txtSoLuong.getText());
            if (soLuong < 0 || soLuong > 200) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0 và bé hơn 200");
                return null;
            }
//            int xx = tblKhuyenMai.getSelectedRow();
//            if (xx >= 0 && soLuong <= listKM.get(xx).getSoHoaDonAD()) {
//                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn bằng số hóa đơn đã áp dụng");
//                return null;
//            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số");
            e.printStackTrace();
            return null;
        }
        Date NgayBatDau = dcsBatDau.getDate();
        Date NgayKetThuc = dcsKetThuc.getDate();
        Date currentDate = new Date();
        if (dcsBatDau.isEnabled()) {
            if (NgayBatDau.before(currentDate)) {
                JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải lớn hơn ngày hôm nay");
                return null;
            }
        }
        if (NgayKetThuc.before(NgayBatDau)) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc phải lớn hơn ngày bắt đầu");
            return null;
        }
        int trangThai = 1; // 1 là trạng thái "Đang hoạt động", 0 là trạng thái "Đã hết hạn"
        if (rdoDangHoatDong.isSelected()) {
            trangThai = 1;
        } else {
            trangThai = 0;
        }
        KhuyenMai km = new KhuyenMai();
        km.setMaKM(txtMaKhuyenMai.getText());
        km.setTenChienDich(txtTenChienDich.getText());
        km.setMucGiamGia(MucGiam);
        km.setNgayBD(dcsBatDau.getDate());
        km.setNgayKT(dcsKetThuc.getDate());
        km.setHoaDonToiThieu(HDTT);
        km.setSoLuong(soLuong);
        km.setTrangThai(trangThai);
        return km;
    }

    private void maAuto() {
        ArrayList<KhuyenMai> ds = kmRepo.getAllKhuyenMai();
        if (ds.size() == 0) {
            txtMaKhuyenMai.setText("KM001");
        } else {
            int viTriCuoi = ds.size() - 1;
            KhuyenMai k = ds.get(viTriCuoi);
            String ma = k.getMaKM();
            int so = Integer.valueOf(ma.substring(2));
            so++;
            String id = "KM00" + so;
            txtMaKhuyenMai.setText(id);
        }

    }

