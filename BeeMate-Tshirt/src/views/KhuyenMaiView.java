    private void loadTable() {
//        ArrayList<KhuyenMai> ds = this.kmRepo.paging(page, limit);
        ArrayList<KhuyenMai> ds = this.kmRepo.getAllKhuyenMai();
        DefaultTableModel dtm = (DefaultTableModel) this.tblKhuyenMai.getModel();
        dtm.setRowCount(0);
        for (KhuyenMai km : ds) {
            Object[] row = {
                km.getMaKM(),
                km.getTenChienDich(),
                km.getMucGiamGia(),
                km.getHoaDonToiThieu(),
                km.getSoLuong(),
                km.getSoHoaDonAD(),
                km.getNgayBD(),
                km.getNgayKT(),
                km.getTrangThai() == 0 ? "Đã hết hạn" : "Đang hoạt động"
            };
            dtm.addRow(row);
        }
    }

    private int getTotalPages() {
        int totalItems = this.kmRepo.getTotalItems();
        return (int) Math.ceil((double) totalItems / limit);
    }

    private void searchByMaKH() {
        String searchMaKH = txtTimKiem.getText().trim();
        DefaultTableModel searchModel = new DefaultTableModel();
        searchModel = (DefaultTableModel) tblKhuyenMai.getModel();
        searchModel.setRowCount(0);

        for (KhuyenMai km : listKM) {
            if (km.getMaKM().toLowerCase().contains(searchMaKH.toLowerCase())) {
                searchModel.addRow(new Object[]{
                    km.getMaKM(),
                    km.getTenChienDich(),
                    km.getMucGiamGia(),
                    km.getHoaDonToiThieu(),
                    km.getSoLuong(),
                    km.getSoHoaDonAD(),
                    km.getNgayBD(),
                    km.getNgayKT(),
                    km.getTrangThai() == 0 ? "Đã hết hạn" : "Đang hoạt động",});
            }
        }
        tblKhuyenMai.setModel(searchModel);
    }

