    private void btnThemKMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKMActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm khuyến mại không?", "Xác nhận yêu cầu", JOptionPane.YES_NO_CANCEL_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            KhuyenMai km = getKhuyenMai();
            if (km == null) {
                JOptionPane.showMessageDialog(this, "Mời bạn thử lại");
                return;
            }
            if (this.kmRepo.checkTrungMa(txtMaKhuyenMai.getText().trim())) {
                JOptionPane.showMessageDialog(this, "Mã KM đã tồn tại");
                return;
            }
            this.kmRepo.InsertKhuyenMai(km);
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadTable();

        }
    }//GEN-LAST:event_btnThemKMActionPerformed

    private void btnSuaKMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaKMActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn chỉnh sửa khuyến mại không?", "Xác nhận yêu cầu", JOptionPane.YES_NO_CANCEL_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            int vtri = tblKhuyenMai.getSelectedRow();
            if (vtri == -1) {
                JOptionPane.showMessageDialog(this, "Bạn phải chọn dòng muốn chỉnh sửa");
                return;
            }
            KhuyenMai km = getKhuyenMai();
            if (km == null) {
                JOptionPane.showMessageDialog(this, "Mời bạn thử lại");
                return;
            }
            int id_km = this.kmRepo.getIdKM(vtri);
            this.kmRepo.UpdateKhuyenMai(km, id_km);
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loadTable();
//                this.kmRepo.UpdateTrangThaiKM(km);
//                listKM = this.kmRepo.getAllKhuyenMai();
//                fillToFormKM(0);

        }
    }//GEN-LAST:event_btnSuaKMActionPerformed

