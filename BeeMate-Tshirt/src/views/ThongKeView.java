    private void cbbThongKeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbThongKeItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String luaChon = (String) cbbThongKe.getSelectedItem();
            if (luaChon == "Từ trước tới nay") {
                doanhThu(1);
                tongHoaDon(1);
                tongSoSanPham(1);
                bieuDo(1);
            } else if (luaChon == "Ngày hôm nay") {
                doanhThu(2);
                tongHoaDon(2);
                tongSoSanPham(2);
                bieuDo(2);
            } else if (luaChon == "Tháng này") {
                doanhThu(3);
                tongHoaDon(3);
                tongSoSanPham(3);
                bieuDo(3);
            } else {
                doanhThu(4);
                tongHoaDon(4);
                tongSoSanPham(4);
                bieuDo(4);
            }
        }
    }//GEN-LAST:event_cbbThongKeItemStateChanged

