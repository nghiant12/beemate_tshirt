    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // Lấy ngày hôm nay
        Date ngayHienTai = new Date();

        // Lấy ngày trực tiếp từ JDateChooser
        Date ngayBD = txtNgayBD.getDate();
        Date ngayKT = txtNgayKT.getDate();

        // Kiểm tra nếu ngày bắt đầu hoặc ngày kết thúc lớn hơn ngày hôm nay
        if (ngayBD.after(ngayHienTai)) {
            // Hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu không được chọn sau ngày hôm nay!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (ngayKT.after(ngayHienTai)) {
            // Hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Ngày kết thúc không được chọn sau ngày hôm nay!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Kiểm tra nếu ngayBD lớn hơn hoặc bằng ngayKT
        if (ngayBD.after(ngayKT)) {
            // Hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải nhở hơn ngày kết thúc và ngược lại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        doanhThu(5);
        tongHoaDon(5);
        tongSoSanPham(5);
        bieuDo(5);
    }//GEN-LAST:event_btnTimActionPerformed

