    private DefaultCategoryDataset createDataset(int check) {//1 từ trước đến nay,2 ngày hôm nay, 3 tháng này, 4 năm nay, 5 tìm kiếm trong khoảng ngày
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        ArrayList<ThongKe> ds = new ArrayList<>();
        ds.clear();
        if (check == 1) {
            ds = thongKeRepo.thongKeTatCaSanPhamBanRaTuTruocDenNay();
        } else if (check == 2) {
            ds = thongKeRepo.thongKeTatCaSanPhamBanRaTrongHomNay();
        } else if (check == 3) {
            ds = thongKeRepo.thongKeTatCaSanPhamBanRaTrongThangNay();
        } else if (check == 4) {
            ds = thongKeRepo.thongKeTatCaSanPhamBanRaTrongNamNay();
        } else if (check == 5) {
            ds = thongKeRepo.thongKeTatCaSanPhamBanTrongKhoangThoiGian(txtNgayBD.getDate(), txtNgayKT.getDate());
        }
        // Thêm dữ liệu mẫu
        for (ThongKe a : ds) {
            dataset.addValue(a.getSoLuong(), "Sản phẩm", a.getTenSP());
        }
        return dataset;
    }

