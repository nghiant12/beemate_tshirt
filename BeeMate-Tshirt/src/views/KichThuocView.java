    private void btnCapNhatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCapNhatMouseClicked
        // TODO add your handling code here:
        if (viTriChon == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng hãy chọn 1 màu trước khi cập nhật!");
            return;
        }
        int xacNhan = JOptionPane.showConfirmDialog(this, "Xác nhận cập nhật!", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (xacNhan == 0) {
            String maSizeKhiChon = (String) tblKichThuoc.getValueAt(viTriChon, 1);
            ktRepo.update_TenSize_VeNull(maSizeKhiChon);
            Size s = getForm(1);
            if (s == null) {
                return;
            }
            ktRepo.update(s);
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            loadTableSize(2);
            clearForm();
        } else {
            return;
        }
    }//GEN-LAST:event_btnCapNhatMouseClicked

