    private void tblKichThuocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKichThuocMouseClicked
        // TODO add your handling code here:
        int viTri = tblKichThuoc.getSelectedRow();
        if (viTri == -1) {
            return;
        }
        viTriChon = viTri;
        txtMaTH.setText((String) tblKichThuoc.getValueAt(viTri, 1));
        txtTenTH.setText((String) tblKichThuoc.getValueAt(viTri, 2));
        if (tblKichThuoc.getValueAt(viTri, 3).equals("Hoạt động")) {
            rdoHD.setSelected(true);
        } else {
            rdoKHD.setSelected(true);
        }
    }//GEN-LAST:event_tblKichThuocMouseClicked

    private void cbbTrangThaiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbTrangThaiItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String trangThai = (String) cbbTrangThai.getSelectedItem();
            if (trangThai == "Tất cả") {
                loadTableSize(2);
            } else if (trangThai == "Hoạt động") {
                loadTableSize(0);
            } else {
                loadTableSize(1);
            }
        }
    }//GEN-LAST:event_cbbTrangThaiItemStateChanged

