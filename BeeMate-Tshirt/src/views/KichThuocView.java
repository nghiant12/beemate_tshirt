    public Size getForm(int chucNang) {//truyền vào 0 cn thêm, 1 là cn cập nhật
        ArrayList<Size> ds = ktRepo.findAllSizeSX();
        String ma = txtMaTH.getText();
        if (chucNang == 0) {
            for (Size a : ds) {
                if (a.getMaSize().equals(ma)) {
                    JOptionPane.showMessageDialog(this, "Mã size này đã tồn tại!");
                    return null;
                }
            }
        }
        String ten = txtTenTH.getText();
        if (ten.trim().isBlank()) {
            JOptionPane.showMessageDialog(this, "Tên size không được để trống!");
            return null;
        }
        if (ten.length() > 100) {
            JOptionPane.showMessageDialog(this, "Tên size không được dài quá 100 kí tự!");
            return null;
        }
        for (Size a : ds) {
            if (chucNang == 1) {
                if (a.getTenSize()== null && a.getMaSize().equals(tblKichThuoc.getValueAt(viTriChon, 1))) {
                    break;
                }
            }
            if (a.getTenSize().equals(ten)) {
                JOptionPane.showMessageDialog(this, "Tên size này đã tồn tại!");
                return null;
            }
        }
        int trangThai;
        if (rdoHD.isSelected()) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }

        Size s = new Size();
        String maSizeKhiChon = (String) tblKichThuoc.getValueAt(viTriChon, 1);
        if (chucNang == 0) {
            s.setMaSize(ma);
        } else {
            s.setMaSize(maSizeKhiChon);
        }
        s.setTenSize(ten);
        s.setTrangThai(trangThai);
        return s;
    }
