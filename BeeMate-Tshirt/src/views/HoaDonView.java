    private void loadTableHD() {
        ArrayList<HoaDon> ds = this.hdRepo.paging(this.pageHd, this.limitHd);
        DefaultTableModel dtm = (DefaultTableModel) this.tblHoaDon.getModel();
        dtm.setRowCount(0);
        for (HoaDon h : ds) {
            Object[] row = {
                h.getMaHD(),
                h.getNv().getHoTen(),
                h.getKh().getHoTen(),
                h.getNgayTao(),
                h.getTongTien(),
                h.getSoTienGiamGia(),
                h.getHinhThucThanhToan(),
                h.getGhiChu(),
                h.getThanhTien(),
                h.getTrangThai() == 0 ? "Chưa thanh toán" : "Đã thanh toán"
            };
            dtm.addRow(row);
        }
    }

    private void loadTableHdct() {
        int rowIndex = this.tblHoaDon.getSelectedRow();
        if (rowIndex == -1) {
            return;
        }
        int id_hd = this.hdRepo.getIdHd(rowIndex);
        ArrayList<HoaDonCT> ds = this.hdctRepo.paging(id_hd, pageHdct, limitHdct);
        DefaultTableModel dtm = (DefaultTableModel) this.tblHdct.getModel();
        dtm.setRowCount(0);
        for (HoaDonCT h : ds) {
            Object[] row = {
                h.getId(),
                h.getSpct().getMaSPCT(),
                h.getSoLuong(),
                h.getSpct().getDonGia()
            };
            dtm.addRow(row);
        }
    }

    private int getTotalPagesHD() {
        int totalItems = this.hdRepo.getTotalItems();
        return (int) Math.ceil((double) totalItems / limitHd);
    }

    private int getTotalPagesHdct() {
        int totalItems = this.hdctRepo.getTotalItems();
        return (int) Math.ceil((double) totalItems / limitHdct);
    }

