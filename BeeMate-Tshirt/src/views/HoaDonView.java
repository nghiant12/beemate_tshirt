    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String searchMa = txtTimKiem.getText().toLowerCase();
        if (searchMa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập dữ liệu cần tìm");
            return;
        }
        ArrayList<HoaDon> ds = hdRepo.findAll();
        DefaultTableModel dtm = (DefaultTableModel) tblHoaDon.getModel();
        dtm.setRowCount(0);
        int check = 0;
        for (HoaDon h : ds) {
            if (h.getMaHD().toLowerCase().contains(searchMa)) {
                check = 1;
                Object[] row = {
                    h.getMaHD(),
                    h.getNv().getHoTen(),
                    h.getKh().getHoTen(),
                    h.getNgayTao(),
                    h.getTongTien(),
                    h.getSoTienGiamGia(),
                    h.getHinhThucThanhToan(),
                    h.getGhiChu(),
                    h.getThanhTien(),
                    h.getTrangThai() == 0 ? "Chưa thanh toán" : "Đã thanh toán"
                };
                dtm.addRow(row);
            }
        }
        if (check == 0) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn!");
            this.loadTableHD();
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnNextHdctActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextHdctActionPerformed
        // TODO add your handling code here:
        int totalPages = getTotalPagesHdct(); // Phương thức để lấy tổng số trang
        if (this.pageHdct < totalPages) {
            this.pageHdct++;
            this.lblPageHdct.setText(String.valueOf(this.pageHdct));
            this.loadTableHdct();
        }
        // Cập nhật trạng thái nút Back
        this.btnBackHdct.setEnabled(this.pageHdct > 1);
        // Cập nhật trạng thái nút Next
        this.btnNextHdct.setEnabled(this.pageHdct < totalPages);
    }//GEN-LAST:event_btnNextHdctActionPerformed

